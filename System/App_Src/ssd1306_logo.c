/**
  ***************************************************************************************************************************************
  * @brief SSD1306 draw logo image
  * @param None
  * @retval None
  ***************************************************************************************************************************************
  */
void SSD1306_Draw_Logo(void) 
{
    if(SSD1306_Get_Init_State())
    {
	    for(uint8_t _block=0U;_block<8U;_block++)
	    {
	    	uint8_t _cmd=0xB0+_block;
		    I2C_Driver_Write(SSD1306_ADDRESS,0x00,I2C_MEMADD_SIZE_8BIT,&_cmd,1U);
		    _cmd=0x00;
		    I2C_Driver_Write(SSD1306_ADDRESS,0x00,I2C_MEMADD_SIZE_8BIT,&_cmd,1U);
		    _cmd=0x10;
		    I2C_Driver_Write(SSD1306_ADDRESS,0x00,I2C_MEMADD_SIZE_8BIT,&_cmd,1U);
            /* Write multi data */
            I2C_Driver_Write(SSD1306_ADDRESS,0x40,I2C_MEMADD_SIZE_8BIT,(uint8_t*)&SSD1306_LOGO[SSD1306_WIDTH*_block],SSD1306_WIDTH);
	    }
    }
}
